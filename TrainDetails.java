class TrainDetails {
    public static void main(String[] args) {

        byte coaches = 18;
        short seats = 1200;
        int trainNumber = 12627;
        long engineNumber = 9876543210L;
        float speed = 110.5f;
        double distance = 450.75;
        char trainType = 'E';
        boolean isExpress = true;
        String trainName = "Karnataka Express";

        System.out.println("Coaches: " + coaches);
        System.out.println("Seats: " + seats);
        System.out.println("Train Number: " + trainNumber);
        System.out.println("Engine Number: " + engineNumber);
        System.out.println("Speed: " + speed);
        System.out.println("Distance: " + distance);
        System.out.println("Train Type: " + trainType);
        System.out.println("Express: " + isExpress);
        System.out.println("Train Name: " + trainName);
    }
}